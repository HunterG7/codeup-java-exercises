public class StringExercise {
    public static void main(String[] args) {
        //1a
        String one = "We don't need no education";
        System.out.println(one.replace("education", "thought control"));

        //1b
        String two = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(two);

        //1c
        String three = "In windows, the main drive is usually C:\\";
        System.out.println(three);

        //1d
        String four = "I can do backslashes \\, double backslashes \\\\," +
                " and the amazing triple backslash \\\\\\!";
        System.out.println(four);

    }
}
